public class NotasAluno {
    private int rgm;
    private String nome;
    private double notaA1;
    private double notaA2;
    
    public int getRgm(){
        return rgm;
    }
    
    public void setRgm(int rgm){
        this.rgm=rgm;
    }
    
    public String getNome(){
        return nome;
    }
    
    public void setNome(String nome){
        this.nome=nome;
    }

    public double getNotaA1() {
        return notaA1;
    }

    public void setNotaA1(double A1) {
        this.notaA1 = A1;
    }

    public double getNotaA2() {
        return notaA2;
    }

    public void setNotaA2(double A2) {
        this.notaA2 = A2;
    }
    
}
